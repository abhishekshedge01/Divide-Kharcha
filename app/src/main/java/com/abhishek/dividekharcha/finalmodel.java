package com.abhishek.dividekharcha;

public class finalmodel {
    private String name;
    private String email;

    public finalmodel() {
    }

    public finalmodel(String name, String email){
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
